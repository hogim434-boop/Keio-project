// ID로 게시글 한 건 조회
export function getPostById(id: string): DummyPost | undefined {
  return DUMMY_POSTS.find((post) => post.id === id)
}

// 게시글 ID로 댓글 목록 조회
export function getCommentsByPostId(postId: string): DummyComment[] {
  return DUMMY_COMMENTS.filter((comment) => comment.postId === postId)
}

// 탭별 게시글 목록 조회
// '最新': createdAt 내림차순, '人気': likeCount 내림차순, '全て': 기본 순서
export function getPostsByTab(tab: PostTab): DummyPost[] {
  switch (tab) {
    case '最新':
      return [...DUMMY_POSTS].sort((a, b) => b.createdAt.localeCompare(a.createdAt))
    case '人気':
      return [...DUMMY_POSTS].sort((a, b) => b.likeCount - a.likeCount)
    case '全て':
    default:
      return DUMMY_POSTS
  }
}
